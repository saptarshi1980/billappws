package in.net.dpl.domain;

public class Application {

	String applicationNumber;
	String applicationDate;
	String applicatantName;
	double connectionLoad;
	String address;
	String mobile;
	double phase;
	String filePath;
	String modifyDate;
	
	
	
	public String getApplicationNumber() {
		return applicationNumber;
	}
	public void setApplicationNumber(String applicationNumber) {
		this.applicationNumber = applicationNumber;
	}
	public String getApplicationDate() {
		return applicationDate;
	}
	public void setApplicationDate(String applicationDate) {
		this.applicationDate = applicationDate;
	}
	public String getApplicatantName() {
		return applicatantName;
	}
	public void setApplicatantName(String applicatantName) {
		this.applicatantName = applicatantName;
	}
	public double getConnectionLoad() {
		return connectionLoad;
	}
	public void setConnectionLoad(double connectionLoad) {
		this.connectionLoad = connectionLoad;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public double getPhase() {
		return phase;
	}
	public void setPhase(double phase) {
		this.phase = phase;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	public String getModifyDate() {
		return modifyDate;
	}
	public void setModifyDate(String modifyDate) {
		this.modifyDate = modifyDate;
	}
	
	
	
	
	
}
